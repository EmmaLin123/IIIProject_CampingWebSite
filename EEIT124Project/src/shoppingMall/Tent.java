package shoppingMall;

public class Tent {
	
	private String tID;
	private String tName;
	private int tPrice;
	private String tBrand;
	private String tSpec;
	private String tfeature;
	private int people;
	private int tQty;
	public String gettID() {
		return tID;
	}
	public void settID(String tID) {
		this.tID = tID;
	}
	public String gettName() {
		return tName;
	}
	public void settName(String tName) {
		this.tName = tName;
	}
	public int gettPrice() {
		return tPrice;
	}
	public void settPrice(int tPrice) {
		this.tPrice = tPrice;
	}
	public String gettBrand() {
		return tBrand;
	}
	public void settBrand(String tBrand) {
		this.tBrand = tBrand;
	}
	public String gettSpec() {
		return tSpec;
	}
	public void settSpec(String tSpec) {
		this.tSpec = tSpec;
	}
	public String getTfeature() {
		return tfeature;
	}
	public void setTfeature(String tfeature) {
		this.tfeature = tfeature;
	}
	public int getPeople() {
		return people;
	}
	public void setPeople(int people) {
		this.people = people;
	}
	public int gettQty() {
		return tQty;
	}
	public void settQty(int tQty) {
		this.tQty = tQty;
	}
	

}
